				<h2>Apa, <i>Sih</i> IPNU - IPPNU Itu??? </h2>
				<br>
				IPNU-IPPNU sebagai organisasi yang bersifat keterpelajaran, kekaderan, kemasyarakatan, kebangsaan dan keagamaan yang berhaluan Islam Ahlussunah Waljamaah, ternyata dalam perkembangannya mengalami perubahan-perubahan yang diakibatkan oleh tuntutan situasi dan kondisi.
                <br>
                <br>Oleh karenanya menjadi kewajiban setiap warga IPNU - IPPNU untuk terus mempelajari perubahan itu, mengkajinya kemudian mencoba untuk mengatisipasinya. Dan tentunya faktor historis sangat mendukung pula apabila warganya juga senantiasa merenunginya, mempelajari motivasi apa yang melatarbelakangi kelahirannya, dan bagaimana perkembangan organisasi ini dari masa ke masa. Karena dari segi historis pula kita akan mampu untuk menentukan langkah dan alternatif apa yang terbaik yang akan kita jadikan saran untuk terus menyebarluaskan IPNU - IPPNU sekaligus wadah generasi muda NU untuk menyalurkan aspirasi sekaligus sebagai media dakwah.
                <br>
                <br>
                Dalam keorganisasiannya,organisasi ini terpisah antara putra dan putri.Untuk putra,masuk ke dalam kelompok organisasi IPNU,karena IPNU adalah kepanjangan dari "Ikatan Pelajar Nahdlatul Ulama" sehingga kelompok ini terdiri dari barisan pelajar putra Nahdlatul Ulama(NU).
                <br>
                Sedangkan untuk putri sendiri masuk kedalam organisasi IPPNU ,karena IPPNU adalah kepanjangan dari "Ikatan Pelajar Putri Nahdlatul Ulama"sehingga  sudah sangat jelas bahwa IPPNU tersebut terdiri dari pelajar putri dengan berfahamkan Ahlusunnah Wal Jama'ah An-Nahdliyah.
                <br>
                <br>

        <b><h3><center><br>Sejarah Singkat IPNU IPPNU</center></h3></b>
        <br>
        IPNU berdiri pada tanggal 24 Februari 1954 bertepatan dengan tanggal 20 Jumadil Akhir 1373 dalam penyelenggaraan Kongres Ma'arif seluruh Indonesia di Semarang. Tokoh-tokoh pendiri IPNU adalah para pelajar dari Yogyakarta, Semarang dan Surakarta yang dipelopori oleh Tolhah Mansur, Muhammad Sofyan Cholil, Mustahal Achmad Masyhud dan Ahmad Ghani Farida. Dalam Konferensi tersebut diproklamirkan berdirinya IPNU dan berhasil menetapkan Tolhah Mansur sebagai pendiri dan ketua umum pertama pimpinan pusat IPNU.
        <br>
        Sedangkan kelahiran IPPNU adalah pada waktu kongres pertama IPNU, yaitu tanggal 2 Maret 1955, bertepatan dengan tanggal 8 Rajab 1374, yang pada mulanya IPPNU hanyalah merupakan bagian dari departemen keputrian IPNU . Pada kongres pertama IPNU tersebut, para aktivis putri dan santri yang dimontori oleh Umroh Mahfudhoh dan mendapatkan dukungan dari ketua Muslimat NU Nyai Hajah Mahmudah Mawardi dan ketua pusat LP Ma'arif NU Kiai Haji Syukri Ghazali mendirikan organisasi yang sejajar dengan IPNU yaitu IPPNU . Sebagai ketua umum pertama pimpinan pusat IPNU adalah  Umroh Mahfudhoh.
        <br>
        Berdirinya IPNU dimaksudkan untuk menyatukan organisasi-organisasi pelajar di bawah naungan NU. Namun pada saat itu, sudah bermunculan beberapa organisasi pelajar di berbagai daerah, dan masih berjuang secara sendiri-sendiri tanpa mengenal satu sama lain. Organisasi-organisasi tersebut di antara lain adalah :
        <br>

            <ul type="disc">
                <li>Tsamarotul Mustafidin di Surabaya, lahir pada tahun 1936.</li>
                <li>PERSANOE atau kepanjangan dari Persatuan santri Nahdlatoel Oelama, lahir pada tahun 1939.</li>
                <li>IMNU atau ikatan menurut Nahdlatul Ulama lahir pada tahun 1947 di Malang.</li>
                <li>IMNU atau ikatan mubaligh Nahdlatul Ulama lahir pada tahun 1950 di Semarang.</li>
                <li>PARPENO (Persatuan Nahdlatoel Oelama) di Kediri.
                </li>
                <li>IPINU (Ikatan Pelajar Islam Nahdlatul Ulama) di Bangil.</li>
                <li>IPNU (Ikatan Pelajar Nahdlatul Ulama) di Medan.</li>
            </ul>
            <br>
            Dalam perjalanannya, IPNU dan IPPNU sejak berdiri mengalami beberapa kali perubahan singkatan nama. Sejak berdiri sampai kongres ke-XI Tahun 1987, IPNU Singkatan dari Ikatan Pelajar Nahdlatul Ulama dan IPPNU singkatan dari Ikatan Pelajar Putri Nahdlatul Ulama. Namun pada tahun 1987, IPNU dan IPPNU diubah menjadi ikatan Putra Nahdlatul Ulama dan ikatan putri putri Nahdlatul Ulama. Perubahan ini dikarenakan Adanya undang-undang nomor 8 tahun 1985 yang menyatakan bahwa organisasi pelajar adalah OSIS. Perubahan ini juga membawa dampak positif bagi IPNU dan IPPNU sebagai organisasi kepemudaan di bawah naungan NU yang beranggotakan remaja berusia antara 13 sampai 25 tahun. Gerakan IPNU dan IPPNU lebih leluasa di tengah-tengah masyarakat karena anggotanya bukan hanya pelajar dan para santri yang masih duduk di bangku sekolah saja, akan tetapi juga para pemuda dan mahasiswa.
            <br>
            <br>
            Pada tahun 2000 dalam Kongres IPNU ke-XIII dan IPPNU ke-XII di Makassar, muncul wacana untuk mengembalikan IPNU dan IPPNU sebagai organisasi pelajar. Munculnya wacana ini disebabkan karena kesadaran bahwa pelajar adalah sumber kader masa depan NU. Pelajar juga menjadi ujung tombak dalam mengembangkan ajaran Islam Ahlussunnah Wal Jamaah. Atas dasar pemikiran itulah maka Pada kongres XIV IPNU dan ke XIII IPPNU di Surabaya, IPNU dan IPPNU dikembalikan sebagai organisasi pelajar dengan nama Ikatan Pelajar Nahdlatul Ulama dan Ikatan Pelajar Putri Nahdlatul Ulama.
